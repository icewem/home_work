# Даны группу учеников. Нужно вывести количество групп и для каждой группы – количество учеников в ней
# Пример вывода:
# Всего 2 группы.
# В группе 2 ученика.
# В группе 3 ученика.

groups = [
  ['Вася', 'Маша'],
  ['Оля', 'Петя', 'Гриша'],
]

number_group = 0

for a in groups:
    number_group += 1
print("Всего {} группы.".format(number_group))

for b in groups:
    print('В группе {} ученика.'.format(len(b)))



