# Задание 3
# Необходимо вывести имена всех учеников из списка, рядом с именем вывести пол ученика

is_male = {
  'Оля': False,  # если True, то пол мужской
  'Петя': True,
  'Вася': True,
  'Маша': False,
}
names = ['Оля', 'Петя', 'Вася', 'Маша']

for check_dict in names:
    if check_dict in is_male:
        if is_male[check_dict] == False:
            print(check_dict,'Женщина')
        if is_male[check_dict] == True:
            print(check_dict,'Мужчина')


